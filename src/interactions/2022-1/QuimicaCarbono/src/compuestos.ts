
class Compuesto {

    nombre: string;
    carbon: number;
    hidrogeno: number;
    pista: string;

    constructor(nombre: string, carbon: number, hidrogeno: number, pista: string) {

        this.nombre = nombre;
        this.carbon = carbon;
        this.hidrogeno = hidrogeno;
        this.pista = pista;
    } 

}

export default Compuesto;